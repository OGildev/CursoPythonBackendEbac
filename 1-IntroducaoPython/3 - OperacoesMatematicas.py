
n1 = int(input("Digite um n1: "))
n2 = int(input("Digite o n2: "))

# Soma 
soma = n1 + n2
print("A soma dos valores ", n1, " + ", n2, "é igual a:", soma)

# Subtração
subtracao = n1 - n2
print("A subtração dos valores ", n1, " - ", n2, "é igual a:", subtracao)

# Divisão
divisao = n1 / n2
print("A divisão dos valores ", n1, " / ", n2, "é igual a:", divisao)

# Divisão Inteira
divisao_inteira = n1 // n2
print("A divisão inteira dos valores ", n1, " // ", n2, "é igual a:", divisao_inteira)

# Multiplicação
multiplicacao = n1 * n2
print("A multiplicação dos valores ", n1, " * ", n2, "é igual a:", multiplicacao)

# Potencialização
potencia = n1 ** n2
print("A potencialização dos valores ", n1, " ** ", n2, "é igual a:", potencia)