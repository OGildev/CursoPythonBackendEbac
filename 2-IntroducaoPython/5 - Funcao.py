

def numeros_aleatorios():
    print("Início da função número aleatórios!")
    lista = [4,5,7,3,11]
    for i in lista:
        print(i) 

def numeros_loteria():
    print("Início da função de números loteria!")
    for i in range(1, 61):
        print(i)

def gerar_palavras_aleatorias():
    print("Início da função de palavras aleatórias!")
    palavras = ["Cachorro", "Gato", "Lagarto", "Peixe", "Cavalo"]
    for i in palavras:
        print(i)

numeros_aleatorios()
numeros_loteria()
gerar_palavras_aleatorias()